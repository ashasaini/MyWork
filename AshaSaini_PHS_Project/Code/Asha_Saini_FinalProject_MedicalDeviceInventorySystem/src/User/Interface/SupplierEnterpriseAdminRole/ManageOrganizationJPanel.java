    private void populateOrganizationTable() {        
        DefaultTableModel model = (DefaultTableModel) organizationjTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : supplierEnterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getOrganizationName();
            
            model.addRow(row);
        }
    }

    private void populateOrganizationTypeComboBox() {
        orgTypejComboBox.removeAllItems();
        for (Type type : Organization.Type.values()){
            if (type.getValue().startsWith("Supplier"))
                orgTypejComboBox.addItem(type);
        }
    }
