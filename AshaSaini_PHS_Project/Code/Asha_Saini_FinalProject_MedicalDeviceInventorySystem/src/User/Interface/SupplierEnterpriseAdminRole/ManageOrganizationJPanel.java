    private void addOrgjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrgjButtonActionPerformed
        // TODO add your handling code here:
        Type type = (Type)orgTypejComboBox.getSelectedItem();
        if(null == type){
            //pending
            return;
        }
        String orgName = organizationNamejTextField.getText();
        if((null == orgName) || (true == orgName.isEmpty())){
            JOptionPane.showMessageDialog(this, "Please enter a valid organization name", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        supplierEnterprise.getOrganizationDirectory().createOrganization(type,orgName);
        populateOrganizationTable();
    }//GEN-LAST:event_addOrgjButtonActionPerformed


